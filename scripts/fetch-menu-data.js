const https = require('https');
const fs = require('fs');
const path = require('path');

const SHEET_URL = 'https://docs.google.com/spreadsheets/d/e/2PACX-1vTeetU-sTKeodCrslGkzITvyC7Ob4ayTf5HdLDqcEEueZfHw4QqzAxbapHBdWq0TYhR6fbvNuL8lqLT/pub?gid=0&single=true&output=csv';

function fetchURL(url) {
    return new Promise((resolve, reject) => {
        https.get(url, (res) => {
            if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
                fetchURL(res.headers.location).then(resolve).catch(reject);
                return;
            }
            let data = '';
            res.setEncoding('utf8');
            res.on('data', chunk => data += chunk);
            res.on('end', () => resolve(data));
        }).on('error', reject);
    });
}

function parseCSV(text) {
    const rows = [];
    let i = 0;
    const n = text.length;

    while (i < n) {
        const row = [];
        while (true) {
            let field = '';
            if (i < n && text[i] === '"') {
                i++;
                while (i < n) {
                    if (text[i] === '"') {
                        i++;
                        if (i < n && text[i] === '"') { field += '"'; i++; }
                        else break;
                    } else {
                        field += text[i++];
                    }
                }
            } else {
                while (i < n && text[i] !== ',' && text[i] !== '\r' && text[i] !== '\n') {
                    field += text[i++];
                }
            }
            row.push(field);
            if (i >= n || text[i] !== ',') break;
            i++;
        }
        if (i < n && text[i] === '\r') i++;
        if (i < n && text[i] === '\n') i++;
        if (row.some(c => c.trim())) rows.push(row);
    }

    return rows;
}

async function main() {
    console.log('Fetching menu data from Google Sheets...');
    const csv = await fetchURL(SHEET_URL);
    const rows = parseCSV(csv);

    if (rows.length < 50) {
        console.error(`Too few rows (${rows.length - 1} items) — aborting to avoid overwriting with incomplete data`);
        process.exit(1);
    }

    const headers = rows[0].map(h => h.trim());
    const data = rows.slice(1).map(row => {
        const obj = {};
        headers.forEach((h, i) => { obj[h] = (row[i] || '').trim(); });
        return obj;
    });

    const output =
        `// Generated by scripts/fetch-menu-data.js — do not edit manually\n` +
        `// Last updated: ${new Date().toISOString()}\n` +
        `const MENU_DATA = ${JSON.stringify(data, null, 2)};\n`;

    const outPath = path.join(__dirname, '..', 'js', 'menu-data.js');
    fs.writeFileSync(outPath, output, 'utf8');
    console.log(`Saved ${data.length} items to js/menu-data.js`);
}

main().catch(err => { console.error('Error:', err.message); process.exit(1); });
